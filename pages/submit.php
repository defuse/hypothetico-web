        Submitting to Hypothetico
    </div>

    <div class="boxcontent">

    <p>
    What sets Hypothetico apart from other e-zines is that we <em>do not</em>
    require submitted articles to have undergone any kind of peer review
    process. We do not even require submitted articles to contain a truth, or to
    be rational. If you have an interesting idea, and you'd like smart people to
    consider it, but you're not sure if it's already been done, or if it's
    fundamentally flawed, then Hypothetico is the perfect place to have it
    published. Hypothetico acts as a &quot;safe zone&quot; for the premature
    publication of strange and controversial ideas.
    </p>

    <p>
    An interesting idea in its raw form, not constrained by the requirement of
    truth or rationality, can lead to new insights in existing fields, and even
    the creation of completely new fields of study. For example, if you have
    a strong argument against something that is widely held to be true, or even
    proven to be true, allowing others to study and refute your argument can
    give them a better understanding of why that thing is true, at a deeper
    level. 
    </p>

    <p>
    This is not to say that the scientific method and mathematical rigor are not
    important. Hypothetico readers must recognize that the published works are
    intended only to provoke thought, and that any insights they get from
    thinking about them must still be rigorously tested before they are used in
    practice or accepted as true.
    </p>

    <p>
    We are particularly interested in:
    </p>

    <ul>
        <li>Isomorphisms between fields.</li>
        <li>Strange ways of thinking of well-known things.</li>
        <li>Strange ways of explaining things.</li>
        <li>Multiple levels of meaning.</li>
        <li>Anything thought-provoking.</li>
    </ul>

    <p>
    Hypothetico only publishes ASCII text files, such as those made with a text
    editor like notepad, Vim, or Emacs. Submissions should be formatted so that
    no line is longer than 80 characters, and should not be longer than 200
    lines. The length requirement may seem harsh, but we are interested in
    thought-provoking ideas, and we want to leave the reader with time to think.
    There is no minimum length. If you can do something really clever in 80
    characters, we'll accept it.
    </p>

    <p>
    Please use correct spelling and grammar, unless not doing so is an
    intentional component of your work. Your submission doesn't have to be
    English prose, though. It could be a poem, sequence of mathematical
    equations, unique ASCII art, puzzle, or even a nonsensical sequence of
    characters that exhibits some interesting pattern.
    </p>

    <p>
    You may submit as many times you want. In fact, we encourage you to submit
    multiple times. 
    </p>

    <p>
    The submission deadline for Issue #1 is <b>January 1, 2014</b>. To submit
    a piece, email it as a .txt attachment to <a
    href="mailto:havoc@defuse.ca">havoc@defuse.ca</a>. If you have a question
    please send it to the same address. Please include "Hypothetico" in the
    subject line.
    </p>

